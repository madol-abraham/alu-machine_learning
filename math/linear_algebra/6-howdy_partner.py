#!/usr/bin/env python3
"""
Module for concatenating two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Parameters:
    arr1 (list): The first array.
    arr2 (list): The second array.

    Returns:
    A new list containing the elements of arr1 followed by arr2.
    """
    return arr1 + arr2
